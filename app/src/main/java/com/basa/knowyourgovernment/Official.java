package com.basa.knowyourgovernment;

public class Official {

    private String office;
    private String name;
    private String party;
    private String addressLineOne;
    private String addressLineTwo;
    private String addressCity;
    private String addressState;
    private String addressZip;
    private String phone;
    private String email;
    private String website;

    public String getOffice() { return this.office; }

    public void setOffice(String s) { this.office = s; }

    public String getName() { return this.name; }

    public void setName(String s) { this.name = s; }

    public String getParty() { return this.party; }

    public void setParty(String s) { this.party = s; }

    public String getAddressLineOne() { return this.addressLineOne; }

    public void setAddressLineOne(String s) { this.addressLineOne = s; }

    public String getAddressLineTwo() { return this.addressLineTwo; }

    public void setAddressLineTwo(String s) { this.addressLineTwo = s; }

    public String getAddressCity() { return this.addressCity; }

    public void setAddressCity(String s) { this.addressCity = s; }

    public String getAddressState() { return this.addressState; }

    public void setAddressState(String s) { this.addressState = s; }

    public String getAddressZip() { return this.addressZip; }

    public void setAddressZip(String s) { this.addressZip = s; }

    public String getPhone() { return this.phone; }

    public void setPhone(String s) { this.phone = s; }

    public String getEmail() { return this.email; }

    public void setEmail(String s) { this.email = s; }

    public String getWebsite() { return this.website; }

    public void setWebsite(String s) { this.website = s; }

}
